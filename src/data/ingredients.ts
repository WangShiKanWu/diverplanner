const fish = (
  id: string,
  nameZh: string,
  source: string,
  fishFarmZone: FishFarmZone,
  priority: number,
  baseValue = priority,
): Ingredient => ({
  id,
  nameZh,
  type: 'fish',
  source,
  farmArea: 'fishFarm',
  priority,
  image: imageByType.fish,
  fishFarmZone,
  baseValue,
  growthDifficulty: Math.max(2, Math.min(9, priority)),
  manualDifficulty: Math.max(3, Math.min(10, priority + 1)),
  spaceCost: Math.max(1, Math.min(6, Math.ceil(priority / 2))),
});

